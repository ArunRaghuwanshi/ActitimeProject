package MethodPractice;

public class Radius {
   public static void main(String[] args) {
	findArea(5);
	findCircumference(7);
}
   public static void findArea(float a)
   {
	   System.out.println(3.14*a*a);
   }
   public static void findCircumference(float b)
   {
	   System.out.println(2*3.14*b);
   }
   
}
